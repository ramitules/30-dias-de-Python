# ENTORNO VIRTUAL

# Para comenzar un proyecto lo ideal es tener un entorno virtual. Un entorno virtual nos permite
# crear un entorno aparte, isolado. Esto nos ayudara a evitar conflictos entre proyectos. Si
# escribimos PIP FREEZE en un terminal veremos todos los paquetes instalados en la computadora.
# Si utilizamos VIRTUALENV accederemos solo a los paquetes que son especificos al proyecto.
